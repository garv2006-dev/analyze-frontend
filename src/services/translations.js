export const translateDynamic = (val, lang) => {
  if (!val) return '';
  const key = val.toString().trim().toUpperCase();
  const langTranslations = translations[lang] || translations.en;
  
  if (key === 'BULLISH' || key === 'BULLISH CONTINUATION') return langTranslations.bullish || val;
  if (key === 'BEARISH' || key === 'BEARISH CORRECTION') return langTranslations.bearish || val;
  if (key === 'SIDEWAYS' || key === 'SIDEWAYS CONSOLIDATION') return langTranslations.sideways || val;
  
  if (key === 'BUY') return langTranslations.buy || val;
  if (key === 'SELL') return langTranslations.sell || val;
  if (key === 'HOLD') return langTranslations.hold || val;
  
  if (key === 'POSITIVE') return langTranslations.positive || val;
  if (key === 'NEGATIVE') return langTranslations.negative || val;
  if (key === 'NEUTRAL') return langTranslations.neutral || val;
  
  if (key === 'UP') return langTranslations.up || val;
  if (key === 'DOWN') return langTranslations.down || val;
  
  return val;
};
